    data = [1, 2, 3, 4, 5]
    cycliclist1 = CyclicList(data)
    data.extend([6, 7, 8, 9, 10])

    count = 0
    for item in cycliclist1:
        if count == 10:
            break
        print(item, end=' ')
        count += 1

